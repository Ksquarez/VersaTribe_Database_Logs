CREATE VIEW [dbo].[Vw_Attendance]
AS
SELECT        dbo.Attendence.Id, dbo.Attendence.Login_Date_Time, dbo.Attendence.Org_Id, dbo.Attendence.Person_Id, dbo.Attendence.TStamp, dbo.Attendence.TOwner, dbo.Attendence.Status, dbo.Attendence.Owner, 
                         dbo.Attendence.Attendence_Type, dbo.FormatFullName(dbo.Person.FirstName, dbo.Person.LastName) AS FullName, dbo.Person.TOwner AS PersonEmail
FROM            dbo.Attendence INNER JOIN
                         dbo.Person ON dbo.Attendence.Person_Id = dbo.Person.Person_Id
GO

