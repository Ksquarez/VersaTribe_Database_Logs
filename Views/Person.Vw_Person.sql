                         dbo.AspNetUsers.Id, dbo.AspNetUsers.EmailConfirmed, dbo.AspNetUsers.Email, dbo.AspNetUsers.UserName, dbo.AspNetUsers.Email AS PersonEmail
FROM            dbo.AspNetUsers INNER JOIN
                         dbo.Person ON dbo.AspNetUsers.UserName = dbo.Person.TOwner
